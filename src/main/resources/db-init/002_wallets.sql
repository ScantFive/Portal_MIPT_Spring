DROP TABLE IF EXISTS wallets CASCADE;

CREATE TABLE IF NOT EXISTS wallets (
  owner            UUID PRIMARY KEY REFERENCES users(id) ON DELETE CASCADE,
  available_tokens BIGINT DEFAULT 0,
  reserved_tokens  BIGINT DEFAULT 0
);

