  // Метод для получения группы категории (Товары/Электроника)
  public String getGroup() {
    String[] parts = displayName.split("/");
    if (parts.length >= 2) {
      return parts[0] + "/" + parts[1];
    }
    return parts[0];
  }

  // Метод для получения подкатегории (Компьютеры)
  public String getSubcategory() {
    String[] parts = displayName.split("/");
    if (parts.length >= 3) {
      return parts[2];
    }
    return "";
  }

  // Метод для получения всех групп категорий по типу
  public static List<String> getGroupsForType(Type type) {
    Set<String> groups = new TreeSet<>();
    String prefix = (type == Type.OBJECTS) ? "Товары" : "Услуги";

    for (Category category : values()) {
      if (category.name().startsWith(prefix.toUpperCase().replace(" ", "_"))) {
        groups.add(category.getGroup());
      }
    }

    return new ArrayList<>(groups);
  }

  // Метод для получения категорий в определенной группе
  public static List<Category> getCategoriesInGroup(String group) {
    List<Category> categories = new ArrayList<>();

    for (Category category : values()) {
      if (category.getGroup().equals(group)) {
        categories.add(category);
      }
    }

    return categories;
  }

  // Метод для получения категорий по типу объявления
  public static List<Category> getCategoriesForType(Type type) {
    List<Category> result = new ArrayList<>();
    String prefix = (type == Type.OBJECTS) ? "ТОВАРЫ" : "УСЛУГИ";

    for (Category category : values()) {
      if (category.name().startsWith(prefix)) {
        result.add(category);
      }
    }

    return result;
  }

  // Метод для безопасного получения Category
  public static Category fromNameSafe(String name) {
    if (name == null || name.trim().isEmpty()) {
      return null;
    }

    try {
      // Пробуем найти по enum name
      return Category.valueOf(name.trim());
    } catch (IllegalArgumentException e) {
      // Пробуем найти по displayName
      for (Category category : values()) {
        if (category.getDisplayName().equals(name.trim())) {
          return category;
        }
      }
      return null;
    }
  }

  // Метод для получения типа объявления по категории
  public static Type getTypeForCategory(Category category) {
    if (category == null) {
      return null;
    }

    String name = category.name();
    if (name.startsWith("ТОВАРЫ")) {
      return Type.OBJECTS;
    } else if (name.startsWith("УСЛУГИ")) {
      return Type.SERVICES;
    }
    return null;
  }
  // Добавьте этот метод в enum Category
  public static Category fromDisplayName(String displayName) {
    if (displayName == null || displayName.trim().isEmpty()) {
      return null;
    }

    for (Category category : values()) {
      if (category.getDisplayName().equals(displayName.trim())) {
        return category;
      }
    }
    return null;
  }
  @Override
  public String toString() {
    return displayName;
  }
